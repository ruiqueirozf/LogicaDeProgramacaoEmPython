def adicao(num, add):
    return num + add

def subtracao(num, sub):
    return num - sub

def multiplicacao(num, mult):
    return num * mult

def divisao(num, divisor):
    return num / divisor


assert adicao(2, 2) == 4
assert subtracao(2, 2) == 0
assert multiplicacao(2, 2) == 4
assert divisao(2, 2) == 1